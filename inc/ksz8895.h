////////////////////////////////////////////////////////////////////////////////
/// @file
/// @brief KSZ8895 Ethernet switch.
////////////////////////////////////////////////////////////////////////////////

#ifndef KSZ8895_H_
#define KSZ8895_H_

// *****************************************************************************
// ************************** System Include Files *****************************
// *****************************************************************************

#include <xc.h>
#include <stdbool.h>
#include <stddef.h>
#include <stdint.h>

// *****************************************************************************
// ************************** User Include Files *******************************
// *****************************************************************************

// *****************************************************************************
// ************************** Macros *******************************************
// *****************************************************************************

// *****************************************************************************
// ************************** Defines ******************************************
// *****************************************************************************

// *****************************************************************************
// ************************** Declarations *************************************
// *****************************************************************************

// *****************************************************************************
// ************************** Function Prototypes ******************************
// *****************************************************************************

////////////////////////////////////////////////////////////////////////////////
/// @brief  Initialize the Ethernet switch operation.
///
/// @return Value of '0' is returned.
///
/// This function initializes the Ethernet switch for operation.
////////////////////////////////////////////////////////////////////////////////
int KSZ8895Init( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Read a KSZ8895 register.
///
/// @param  address
///             KSZ8895 register address to read.
/// @param  data
///             Buffer to store read data.
///
/// @return Identification of read operation failure:
///             -1 - Communication failure.
///             0  - Register read success.
///
/// This function read a register from the KSZ8895 and populated its value
/// within \p data.
////////////////////////////////////////////////////////////////////////////////
int KSZ8895ReadReg(uint8_t address, uint8_t *data);

////////////////////////////////////////////////////////////////////////////////
/// @brief  Write a KSZ8895 register.
///
/// @param  address
///             KSZ8895 register address to write.
/// @param  data
///             Data to write.
///
/// @return Identification of write operation failure:
///             -1 - Communication failure.
///             0  - Register write success.
///
/// This function writes \p data to a register from the KSZ8895.
////////////////////////////////////////////////////////////////////////////////
int KSZ8895WriteReg(uint8_t address, uint8_t data);

////////////////////////////////////////////////////////////////////////////////
/// @brief  Reset the KSZ8895.
///
/// This function toggle the KSZ8895 pin RST_N to reset the device.
////////////////////////////////////////////////////////////////////////////////
void KSZ8895Reset( void );

#endif // KSZ8895_H_
